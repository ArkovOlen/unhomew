#include <iostream>
#include <cmath>

using namespace std;

int main() {
    setlocale(LC_ALL, "");
    double e, f, g, h; // Переменные для ввода данных
    double a, b, c; // Переменные для результатов функций

    // Запрашиваем ввод данных от пользователя
    cout << "Введите значение переменной e: ";
    cin >> e;
    cout << "Введите значение переменной f: ";
    cin >> f;
    cout << "Введите значение переменной g: ";
    cin >> g;
    cout << "Введите значение переменной h: ";
    cin >> h;

    // Вычисление функции a
    a = (e + f / 2) / 3;

    // Вычисление функции b
    b = abs(pow(h, 2) - g); // pow для возведения в степень, abs для модуля

    // Вычисление функции c
    double insideC = pow(g - h, 2) - 3 * sin(e); // pow для возведения в степень, sin для синуса
    if (insideC < 0) {
        cerr << "Ошибка: выражение под корнем отрицательное (недействительное значение)." << endl;
        return 1; // Завершаем программу с кодом ошибки
    }
    c = sqrt(insideC); // sqrt для квадратного корня

    // Вывод результатов
    cout << "Значение функции a: " << a << endl;
    cout << "Значение функции b: " << b << endl;
    cout << "Значение функции c: " << c << endl;

    return 0; // Завершение программы
}
