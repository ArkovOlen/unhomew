arr = [12, 45, 100, 78, 952, 6, 333, 777, 222, 1010, 1, 7]

# Печатаем второй, четвертый и т.д. элементы
print("Второй, четвертый и т.д. элементы:", end=" ")
for i in range(1, len(arr), 2):
    print(arr[i], end=" ")
print()

# Печатаем третий, шестой и т.д. элементы
print("Третий, шестой и т.д. элементы:", end=" ")
for i in range(2, len(arr), 3):
    print(arr[i], end=" ")
print()
